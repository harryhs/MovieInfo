package io.thappx.movieinfo.model;

public class CastModel {
	private String cast;
	private long movieId;

	public String getCast() {
		return cast;
	}

	public void setCast(String pCast) {
		cast = pCast;
	}

	public long getMovieId() {
		return movieId;
	}

	public void setMovieId(long pMovieId) {
		movieId = pMovieId;
	}
}
